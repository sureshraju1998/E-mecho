function myFunction() {
    var x = document.getElementById("myInput");
    if (x.type === "password") {
      x.type = "text";
      document.getElementById("icon1").className = "fa fa-eye-slash";
    } else {
      x.type = "password";
      document.getElementById("icon1").className = "fa fa-eye";
    }
  }
